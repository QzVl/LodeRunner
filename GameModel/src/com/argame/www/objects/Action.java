package com.argame.www.objects;

/**
 * Actions that can do {@link DynamicGameObject}.
 */
public enum Action {
    /**
     * Move up.
     */
    MOVE_UP,
    /**
     * Move down.
     */
    MOVE_DOWN,
    /**
     * Move left.
     */
    MOVE_LEFT,
    /**
     * Move right.
     */
    MOVE_RIGHT,
    /**
     * Dig left.
     */
    DIG_LEFT,
    /**
     * Dig right.
     */
    DIG_RIGHT,

    /**
     * Inaction.
     */
    NONE

}
